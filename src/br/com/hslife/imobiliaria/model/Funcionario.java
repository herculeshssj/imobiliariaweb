package br.com.hslife.imobiliaria.model;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.PrimaryKeyJoinColumn;
import javax.persistence.Table;

@Entity
@Table(name="Funcionario")
@PrimaryKeyJoinColumn(name="idPessoaFisica")
@NamedQueries(value={
		@NamedQuery(name="funcionario.buscarPorCPF", query="from Funcionario f where f.cpf = :cpf"),
		@NamedQuery(name="funcionario.buscarTodosAtivos", query="from Funcionario f where f.ativo = true"),
		@NamedQuery(name="funcionario.desativarLogins", query="update Usuario set ativo = false where funcionario.id = :id")
})
public class Funcionario extends Cliente implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 2434867170114747896L;

	@Column(length=10, nullable=false)
	String matricula;
	
	@Column(length=30, nullable=false)
	String cargo;
	
	public Funcionario() {
		tipoCliente = "funcionario";
	}

	public String getMatricula() {
		return matricula;
	}

	public void setMatricula(String matricula) {
		this.matricula = matricula;
	}

	public String getCargo() {
		return cargo;
	}

	public void setCargo(String cargo) {
		this.cargo = cargo;
	}
}