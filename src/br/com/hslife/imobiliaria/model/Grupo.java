package br.com.hslife.imobiliaria.model;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.Date;
import java.util.List;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;

import org.hibernate.annotations.Cascade;
import org.hibernate.annotations.CascadeType;

@Entity
@Table(name="Grupo")
@NamedQueries(value={
		@NamedQuery(name="grupo.defineGrupoPadraoQ1", query="update Grupo set padrao = false"),
		@NamedQuery(name="grupo.defineGrupoPadraoQ2", query="update Grupo set padrao = true where id = :id"),
		@NamedQuery(name="grupo.alteraGrupoPadrao", query="update Usuario set idGrupo = (select id from Grupo where padrao = true) where idGrupo = :id")
})
public class Grupo implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -736210581159729538L;

	@Id
	@GeneratedValue(strategy=GenerationType.IDENTITY)
	Long id;
	
	@Column(length=30, nullable=false)
	String descricao;
	
	@Column
	Boolean padrao;
	
	@Temporal(TemporalType.TIMESTAMP)
	Date dataCriacao;
	
	@OneToMany
	@Cascade(CascadeType.ALL)	
	List<Permissao> permissoes;
	
	public Grupo() {
		dataCriacao = new Date();
		permissoes = new ArrayList<Permissao>();
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

	public Boolean getPadrao() {
		return padrao;
	}

	public void setPadrao(Boolean padrao) {
		this.padrao = padrao;
	}

	public Date getDataCriacao() {
		return dataCriacao;
	}

	public void setDataCriacao(Date dataCriacao) {
		this.dataCriacao = dataCriacao;
	}

	public List<Permissao> getPermissoes() {
		return permissoes;
	}

	public void setPermissoes(List<Permissao> permissoes) {
		this.permissoes = permissoes;
	}
}
