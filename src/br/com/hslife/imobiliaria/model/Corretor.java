package br.com.hslife.imobiliaria.model;

import java.io.Serializable;
import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.PrimaryKeyJoinColumn;
import javax.persistence.Table;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;

@Entity
@Table(name="Corretor")
@PrimaryKeyJoinColumn(name="idPessoaFisica")
@NamedQueries(value={
		@NamedQuery(name="corretor.buscarPorCPF", query="from Corretor c where c.cpf = :cpf"),
		@NamedQuery(name="corretor.buscarTodosAtivos", query="from Corretor c where c.ativo = true")
})
public class Corretor extends Cliente implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6558196958572144653L;

	@Column(length=12)
	String creci;
	
	@Column(length=5)
	String regiao;
	
	@Temporal(TemporalType.DATE)
	Date dataRegistro;
	
	public Corretor() {
		tipoCliente = "corretor";
	}

	public String getCreci() {
		return creci;
	}

	public void setCreci(String creci) {
		this.creci = creci;
	}

	public String getRegiao() {
		return regiao;
	}

	public void setRegiao(String regiao) {
		this.regiao = regiao;
	}

	public Date getDataRegistro() {
		return dataRegistro;
	}

	public void setDataRegistro(Date dataRegistro) {
		this.dataRegistro = dataRegistro;
	}
}